    private void btnGirisActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGirisActionPerformed
        // TODO add your handling code here:
        
        
        try {
            // TODO add your handling code here:
            Pattern kAdiGirisDesen = Pattern.compile("^[a-zA-Z]+$");
            Pattern sifreGirisDesen = Pattern.compile("[0-9]");
            Matcher kAdiGirisYakala = kAdiGirisDesen.matcher(txtKullaniciAdi.getText());
            Matcher sifreGirisYakala = sifreGirisDesen.matcher(txtSifre.getText());
            if (!kAdiGirisYakala.find()) {

                lblKullaniciAdiDurum1.setText("Yanlış Bilgi Girilmiştir.");

            } else {
                lblKullaniciAdiDurum1.setText("");
            }
            if (!sifreGirisYakala.find()) {

                lblSifreDurum.setText("Yanlış Bilgi Girilmiştir.");

            } else {
                lblSifreDurum.setText("");
            }

            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/KitapTakim ","kitap","kitap");
            String sqlSorgu = "SELECT * FROM KULLANICILAR WHERE KULLANICIADI=?";
            PreparedStatement psSorgu = con.prepareStatement(sqlSorgu);
            psSorgu.setString(1,txtKullaniciAdi.getText());
            ResultSet rs = psSorgu.executeQuery();

            if (rs.next()) {

                String KULLANICIADI = rs.getString("KULLANICIADI");
                String SIFRE = String.valueOf(rs.getInt("SIFRE"));
                if (KULLANICIADI.equals(txtKullaniciAdi.getText()) && SIFRE.equals(txtSifre.getText())) {
                    YayınTakip yt = new YayınTakip();

                    yt.setVisible(true);
                    this.dispose();

                } else  {
                    JOptionPane.showMessageDialog(this, "Bilgiler Doğru Girilmemiştir..");
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(KullaniciGiris.class.getName()).log(Level.SEVERE, null, ex);
        }
      
    }//GEN-LAST:event_btnGirisActionPerformed

