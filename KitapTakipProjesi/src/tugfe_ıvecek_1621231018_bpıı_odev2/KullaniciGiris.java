    private void btnKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKaydetActionPerformed
        // TODO add your handling code here:
        Pattern tcDesen = Pattern.compile("[0-9]");
        Matcher tcYakala = tcDesen.matcher(txtTc.getText());
        if(!tcYakala.find()){
           lblTcKontrol.setText("Geçerli bir TC Numarası Giriniz.");
        }else {
           lblTcKontrol.setText("");
        }
        Pattern kAdiDesen = Pattern.compile("^[a-zA-Z]+$");
        Matcher kAdiYakala = kAdiDesen.matcher(txtKullaniciAdiEkle.getText());
        if(!kAdiYakala.find()){
           lblKullaniciAdiKontrol.setText("Geçerli bir Kullanıcı Adı Giriniz.");
        }else {
           lblKullaniciAdiKontrol.setText("");
        }
        Pattern sifreDesen = Pattern.compile("[0-9]");
        Matcher sifreYakala = sifreDesen.matcher(txtSifreEkle.getText());
        if(!sifreYakala.find()){
           lblSifreKontrol.setText("Geçerli bir Şifre Giriniz.");
        }else {
           lblSifreKontrol.setText("");
        }
        Pattern AdiDesen = Pattern.compile("^[a-zA-Z]+$");
        Matcher AdiYakala = AdiDesen.matcher(txtAdiEkle.getText());
        if(!AdiYakala.find()){
           lblAdiKontrol.setText("Geçerli bir Adı Giriniz.");
        }else {
           lblAdiKontrol.setText("");
        }
        Pattern soyadiDesen = Pattern.compile("^[a-zA-Z]+$");
        Matcher soyadiYakala = soyadiDesen.matcher(txtSoyadiEkle.getText());
        if(!soyadiYakala.find()){
           lblSoyadiKontrol.setText("Geçerli bir Soyadı Giriniz.");
        }else {
           lblSoyadiKontrol.setText("");
        }
        Pattern mailDesen = Pattern.compile("[a-zA-Z@]");
        Matcher mailYakala = mailDesen.matcher(txtMailEkle.getText());
        if(!mailYakala.find()){
           lblMailKontrol.setText("Geçerli bir Soyadı Giriniz.");
        }else {
           lblMailKontrol.setText("");
        }
        
        
        try {
            Connection con = DriverManager.getConnection("jdbc:derby://localhost:1527/KitapTakim ","kitap","kitap");
            String sorgu = "SELECT * FROM KULLANICILAR WHERE TC=?";
            PreparedStatement psSorgu = con.prepareStatement(sorgu);
            psSorgu.setInt(1, Integer.parseInt(txtTc.getText()));
            ResultSet rs = psSorgu.executeQuery();
            
            if(rs.next()){
                String sqlUpdate ="UPDATE kullanıcılar" +"SET KULLANICIADI=?,SIFRE=?,ADI=?,SOYADI=?,MAIL=?"+"WHERE TC=?";
                PreparedStatement  psUpdate = con.prepareStatement(sqlUpdate);
                psUpdate.setInt(1, Integer.parseInt(txtTc.getText()));
                psUpdate.setString(2, txtKullaniciAdiEkle.getText());
                psUpdate.setInt(3, Integer.parseInt(txtSifreEkle.getText()));
                psUpdate.setString(4, txtAdiEkle.getText());
                psUpdate.setString(5, txtSoyadiEkle.getText());
                psUpdate.setString(6, txtMailEkle.getText());
                
                psUpdate.executeUpdate();
            }else{
                String sqlInsert ="INSERT INTO KULLANICILAR" +"(TC,KULLANICIADI,SIFRE,ADI,SOYADI,MAIL)"
                        + "VALUES (?,?,?,?,?,?)";
                PreparedStatement  psInsert = con.prepareStatement(sqlInsert);
                psInsert.setInt(1, Integer.parseInt(txtTc.getText()));
                psInsert.setString(2, txtKullaniciAdiEkle.getText());
                psInsert.setInt(3, Integer.parseInt(txtSifreEkle.getText()));
                psInsert.setString(4, txtAdiEkle.getText());
                psInsert.setString(5, txtSoyadiEkle.getText());
                psInsert.setString(6, txtMailEkle.getText());
                
                psInsert.executeUpdate();
                
                txtTc.setText("");
                txtKullaniciAdiEkle.setText("");
                txtSifreEkle.setText("");
                txtAdiEkle.setText("");
                txtSoyadiEkle.setText("");
                txtMailEkle.setText("");
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(KullaniciGiris.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnKaydetActionPerformed

